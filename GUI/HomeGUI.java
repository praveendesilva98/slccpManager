    private void ButtonSearchPaymentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonSearchPaymentMouseClicked
        if(typeBoxPayment.getSelectedItem().toString() == "Select")
        {
            searchPayment.setText(null);
            operationDAO.selectAllPayments(operation, PaymentTable, totalPayment);
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "ID")
        {
            type = "operation.id_operation";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Amount")
        {
            type = "amount";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Date")
        {
            type = "date";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Method")
        {
            type = "method";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Location")
        {
            type = "location";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Name")
        {
            type = "name";
        }
        else if(typeBoxPayment.getSelectedItem().toString() == "Surname")
        {
            type = "surname";
        }

        value_finances = searchPayment.getText();

        
        if(value_finances.isEmpty())
        {
            operationDAO.selectAllPayments(operation, PaymentTable, totalPayment);
        }
        else
        {
            operationDAO.searchPayments(operation, PaymentTable, type, value_finances, totalSelectedPayment);
        }
    }//GEN-LAST:event_ButtonSearchPaymentMouseClicked

