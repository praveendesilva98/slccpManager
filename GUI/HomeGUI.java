    private void ButtonDetailsFinancesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonDetailsFinancesMouseClicked
        int row = FinancesTable.getSelectedRow();
        int id = Integer.parseInt(FinancesTable.getModel().getValueAt(row, 0).toString());      
        String date = FinancesTable.getModel().getValueAt(row, 1).toString();
        String type_operation = FinancesTable.getModel().getValueAt(row, 2).toString();
        String reason = FinancesTable.getModel().getValueAt(row, 3).toString();
        String method = FinancesTable.getModel().getValueAt(row, 4).toString();
        String location = FinancesTable.getModel().getValueAt(row, 5).toString();
        String amount = FinancesTable.getModel().getValueAt(row, 6).toString();
        
        if("Income".equals(type_operation))
        {
            recipientText.setVisible(false);
            recipientLabel.setVisible(false);
            givenText.setVisible(true);
            givenLabel.setVisible(true);
            payerText.setVisible(true);
            payerLabel.setVisible(true);
        }
        else
        {
            givenText.setVisible(false);
            givenLabel.setVisible(false);
            payerText.setVisible(false);
            payerLabel.setVisible(false);
            recipientText.setVisible(true);
            recipientLabel.setVisible(true);
        }
      
        idLabel.setText(Integer.toString(id));
        amountLabel.setText(amount+" €");
        dateLabel.setText(date);
        typeLabel.setText(type_operation);
        methodLabel.setText(method);
        reasonLabel.setText(reason);
        locationLabel.setText(location);

        operationDAO.operationDetails(operation, id, payerLabel, givenLabel, recipientLabel);
      
        PanelFinances.setVisible(false);
        DetailsOperation.setVisible(true);
        
        

    }//GEN-LAST:event_ButtonDetailsFinancesMouseClicked

