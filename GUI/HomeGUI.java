    private void ButtonSearchFinancesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonSearchFinancesMouseClicked
        
        if(typeBoxFinances.getSelectedItem().toString() == "Select")
        {
            searchFinances.setText(null);
            operationDAO.selectAll(operation, FinancesTable, ba, cash, sa);
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "ID")
        {
            type = "id_operation";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Amount")
        {
            type = "amount";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Date")
        {
            type = "date";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Type")
        {
            type = "type";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Reason")
        {
            type = "reason";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Method")
        {
            type = "method";
        }
        else if(typeBoxFinances.getSelectedItem().toString() == "Location")
        {
            type = "location";
        }


        date_1 = ((JTextField)date1.getDateEditor().getUiComponent()).getText();
        date_2 = ((JTextField)date2.getDateEditor().getUiComponent()).getText();
        value_finances = searchFinances.getText();

        
        if(value_finances.isEmpty())
        {
            operationDAO.searchByDate(operation, FinancesTable, date_1, date_2, selectBA, selectCash, selectSA);
        }
        else if(date_1.isEmpty() && date_2.isEmpty())
        {
            operationDAO.searchByType(operation, FinancesTable, type, value_finances, selectBA, selectCash, selectSA);
        }
        else
        {
            operationDAO.searchByDateType(operation, FinancesTable, date_1, date_2, type, value_finances, selectBA, selectCash, selectSA);
        }

    }//GEN-LAST:event_ButtonSearchFinancesMouseClicked

